    /**
     * A fresh glyph node for {@code fileName}, reflecting its type. Falls back to the generic
     * document glyph for unknown types.
     */
    public static Node forFileName(String fileName) {
        String path = PATHS.get(iconKeyFor(fileName));
        return path == null ? Icons.fileSheet() : Icons.of(path);
    }

    /**
     * The glyph key for {@code fileName} — pure (no toolkit), so it is unit-testable. Non-language
     * file types (images, archives, PDFs, …) are resolved by extension first; otherwise the file's
     * {@link LanguageRegistry#forFileName language id} chooses the key. {@code "generic"} (and any
     * unmapped key) renders the fallback document glyph.
     */
    public static String iconKeyFor(String fileName) {
        if (fileName == null) {
            return "generic";
        }
        String base = fileName;
        int slash = Math.max(base.lastIndexOf('/'), base.lastIndexOf('\\'));
        if (slash >= 0) {
            base = base.substring(slash + 1);
        }
        String ext = "";
        int dot = base.lastIndexOf('.');
        if (dot >= 0 && dot < base.length() - 1) {
            ext = base.substring(dot + 1).toLowerCase(Locale.ROOT);
        }

        // Non-language file types, resolved by extension first (these never reach a language id, or
        // would map to the wrong one — e.g. .svg is XML to the highlighter but an image to the user).
        switch (ext) {
            case "png", "jpg", "jpeg", "gif", "bmp", "webp", "ico", "icns", "svg":
                return "image";
            case "pdf":
                return "pdf";
            case "zip", "tar", "gz", "tgz", "bz2", "xz", "7z", "rar", "jar", "war":
                return "archive";
            case "csv", "tsv":
                return "table";
            case "lock":
                return "lock";
            case "txt", "text", "log":
                return "text";
            default:
                break;
        }

        // Otherwise key off the language id (which doubles as the LSP languageId for many types).
        return switch (LanguageRegistry.forFileName(fileName)) {
            case "java" -> "java";
            case "python" -> "python";
            case "javascript", "javascriptreact" -> "javascript";
            case "typescript", "typescriptreact" -> "typescript";
            case "go" -> "go";
            case "php" -> "php";
            case "ruby" -> "ruby";
            case "c" -> "c";
            case "cpp" -> "cpp";
            case "csharp" -> "csharp";
            case "kotlin" -> "kotlin";
            case "html" -> "html";
            case "css" -> "css";
            case "markdown" -> "markdown";
            case "yaml" -> "yaml";
            case "json" -> "json";
            case "xml" -> "code";
            case "shell" -> "shell";
            case "powershell", "batchfile" -> "terminal";
            case "dockerfile" -> "docker";
            case "terraform" -> "terraform";
            case "mermaid" -> "mermaid";
            case "sql" -> "storage";
            case "ini", "properties", "toml", "systemd", "desktop" -> "settings";
            case "rust", "lua", "groovy", "http" -> "code";
            default -> "generic";
        };
    }
